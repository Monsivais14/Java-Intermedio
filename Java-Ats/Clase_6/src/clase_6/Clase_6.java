
package clase_6;

/*
Tipos de datos no primitivos
*/
public class Clase_6 {
    
    public static void main(String[] args) {
        Integer numero = null;
        //Integer no es primitivo, por lo tanto puede almacenar null
        
        System.out.println("El numero es: "+numero);
        
        //cadenas de caracteres
        String palabra = "Hola"; //String es un tipo de dato no primitivo
        System.out.println("Cadena: "+palabra);
        
    }
    
}
